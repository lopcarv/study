public class Missao3_Calculadora {
	//declarando os atributos da classe
	private double R, N1, N2;
	
	public double SOMAR(double N1, double N2)
	{ // inicio do método SOMAR
		// aqui deve ser inserido o bloco de código SOMA
		R = N1 + N2;
		return R;
		
	} //fim do método Soma
	
	public double SUBTRAIR(double N1, double N2)
	{ // inicio do método SUBTRAIR
		// aqui deve ser inserido o bloco de código SOMA
		R = N1 - N2;
		return R;
	} //fim do método Subtração
	
	public double MULTIPLICAR(double N1, double N2)
	{ // inicio do método MULTIPLICAR
		// aqui deve ser inserido o bloco de código MULTIPLICA
		R = N1 * N2;
		return R;
	} //fim do método MULTIPLICAR
	
	public double DIVIDIR(double N1, double N2)
	{ // inicio do método DIVIDIR
		// aqui deve ser inserido o bloco de código DIVIDE
		R = N1 / N2;
		return R;
	} //fim do método dividir
	
}
