# Armazenado Nomes de Alunos

nomes_alunos = ["Ana", "João", "Maria", "Pedro", "Beatriz", "Carlos", "Gabriela", "Bruno", "Clara", "Diego"]

# Acessando um aluno por índice:
primeiro_aluno = nomes_alunos[0]  # "Ana"
ultimo_aluno = nomes_alunos[-1]  # "Diego"
# os negativo representam o final pro inicio


# Percorrendo todos os alunos:
for aluno in nomes_alunos:
    print(f"Aluno(a): {aluno}")
# mostrando o primeiro e ultimos alunos:
print (primeiro_aluno)
print (ultimo_aluno)