# Array de notas dos alunos
notas_alunos = [5.8, 10.5, 6.0, 5.1, 6.0, 9.3, 6.9, 10.0]

# Calculando a soma das notas
soma_notas = sum(notas_alunos)

# Calculando a média das notas
media_notas = soma_notas / len(notas_alunos)

# Mostrando a média das notas
print(f"Média da turma: {media_notas:.2f}")
