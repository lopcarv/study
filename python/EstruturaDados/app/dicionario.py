# Demonstração de Dicionários
dicionario = {"a": 1, "b": 2, "c": 3}
print("Dicionário original:", dicionario)

# Adicionar par chave-valor
dicionario["d"] = 4
print("Após adicionar d:4:", dicionario)

# Remover par chave-valor
del dicionario["b"]
print("Após remover b:", dicionario)

# Acessar valor
print("Valor associado à chave 'a':", dicionario["a"])
